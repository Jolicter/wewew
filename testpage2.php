        <li>Оценка экологического риска и прогнозирование последствий природных и антропогенных катастроф.</li>
        <li>Разработка и внедрение мер по охране окружающей среды и устойчивому использованию природных ресурсов.</li>
        <li>Проведение экологических исследований и мониторинга для оценки качества окружающей среды.</li>
        <li>Сотрудничество с государственными и негосударственными организациями для разработки и реализации экологических программ и проектов.</li>
    </ul>
    
